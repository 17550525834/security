package com.zc.tarf530.model;

public class ReportResponse {

    /**
     * code : 0
     * msg : 上报成功
     * res : null
     */

    private int code;
    private String msg;
    private int res;

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }

    public int getRes() {
        return res;
    }

    public void setRes(int res) {
        this.res = res;
    }
}
