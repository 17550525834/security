package com.zc.tarf530.model;

public class SubmitResponse {

    /**
     * code : 0
     * res : null
     */

    private int code;
    private String msg;
    private String res;

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }

    public String getRes() {
        return res;
    }

    public void setRes(String res) {
        this.res = res;
    }
}
